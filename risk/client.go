var (
	jsonCheck       = regexp.MustCompile(`(?i:(?:application|text)/(?:vnd\.[^;]+\+)?json)`)
	xmlCheck        = regexp.MustCompile(`(?i:(?:application|text)/xml)`)
	queryParamSplit = regexp.MustCompile(`(^|&)([^&]+)`)
	queryDescape    = strings.NewReplacer("%5B", "[", "%5D", "]")
)

// APIClient manages communication with the PingOne Platform API - PingOne Risk API v2021-10-17
// In most cases there should be only one, shared, APIClient.
type APIClient struct {
	cfg    *Configuration
	common service // Reuse a single struct instead of allocating one for each service on the heap.

	// API Services

	RiskAdvancedPredictorsApi *RiskAdvancedPredictorsApiService

	RiskEvaluationsApi *RiskEvaluationsApiService

	RiskPoliciesApi *RiskPoliciesApiService
}

type service struct {
	client *APIClient
}

// NewAPIClient creates a new API client. Requires a userAgent string describing your application.
// optionally a custom http.Client to allow for advanced features such as caching.
func NewAPIClient(cfg *Configuration) *APIClient {
	if cfg.HTTPClient == nil {

		if v := cfg.ProxyURL; v != nil && *v != "" {
			// Parse the proxy URL
			proxyURLParsed, err := url.Parse(*v)
			if err != nil {
				fmt.Println("Failed to parse proxy URL:", err)
				return nil
			}

			// Create a new Transport object with the proxy settings
			transport := &http.Transport{
				Proxy: http.ProxyURL(proxyURLParsed),
			}

			// Create a new HTTP client using the custom Transport
			cfg.HTTPClient = &http.Client{
				Transport: transport,
			}
		} else {
			cfg.HTTPClient = http.DefaultClient
		}
	}

	c := &APIClient{}
	c.cfg = cfg
	c.common.client = c

	// API Services
	c.RiskAdvancedPredictorsApi = (*RiskAdvancedPredictorsApiService)(&c.common)
	c.RiskEvaluationsApi = (*RiskEvaluationsApiService)(&c.common)
	c.RiskPoliciesApi = (*RiskPoliciesApiService)(&c.common)

	return c
}

// selectHeaderContentType select a content type from the available list.
func selectHeaderContentType(contentTypes []string) string {
	returnVar := ""

	if len(contentTypes) > 0 {
		returnVar = contentTypes[0] // use the first content type specified in 'consumes'
	}
	if contains(contentTypes, "application/json") {
		returnVar = "application/json"
	}
	return returnVar
}

// selectHeaderAccept join all accept types and return
func selectHeaderAccept(accepts []string) string {
	if len(accepts) == 0 {
		return ""
	}

	if contains(accepts, "application/json") {
		return "application/json"
	}

	return strings.Join(accepts, ",")
}

// contains is a case insensitive match, finding needle in a haystack
func contains(haystack []string, needle string) bool {
	for _, a := range haystack {
		if strings.EqualFold(a, needle) {
			return true
		}
	}
	return false
}

func parameterValueToString(obj interface{}, key string) string {
	if reflect.TypeOf(obj).Kind() != reflect.Ptr {
		return fmt.Sprintf("%v", obj)
	}
	var param, ok = obj.(MappedNullable)
	if !ok {
		return ""
	}
	dataMap, err := param.ToMap()
	if err != nil {
		return ""
	}
	return fmt.Sprintf("%v", dataMap[key])
}

// callAPI do the request.
func (c *APIClient) callAPI(request *http.Request) (*http.Response, error) {
	resp, err := c.cfg.HTTPClient.Do(request)
	if err != nil {
		return resp, err
	}

	return resp, err
}

